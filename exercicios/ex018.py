from math import radians, sin, cos, tan

angulo = float(input('Qual o 창gulo desejado: '))
seno = sin(radians(angulo))
print('O 창ngulo de {} tem o seno de {:.2f}'
      .format(angulo, seno))

cos = cos(radians(angulo))
print('O 창ngulo de {} tem o seno de {:.2f}'
      .format(angulo, cos))

tan = tan(radians(angulo))
print('O 창ngulo de {} tem o seno de {:.2f}'
      .format(angulo, tan))